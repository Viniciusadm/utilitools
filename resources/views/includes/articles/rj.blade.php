    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        O que é RG?
    </h3>

    <p class="mb-2 sm:mb-3">
        O RG é um documento de identificação emitido pela Secretaria de Segurança Pública de cada estado. O RG é um documento de identificação que contém o nome, data de nascimento, filiação, naturalidade, nacionalidade, sexo, estado civil, profissão, endereço, número do RG, data de emissão, órgão emissor, foto, impressão digital, assinatura, e o número do CPF.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Como obter um RG?
    </h3>

    <p class="mb-2 sm:mb-3">
        Como cada estado emite seus próprios RGs, é difícil dizer de maneira geral como funcionaria. De maneira geral para obter um RG, é necessário ir até um posto de atendimento do Instituto de Identificação da Secretaria de Segurança Pública do estado. O RG é emitido na hora, e é necessário levar um documento de identificação com foto, como a carteira de motorista, carteira de trabalho, carteira de identidade, ou passaporte.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Como é formado o RG IFP RJ?
    </h3>

    <p class="mb-2 sm:mb-3">
        O RG é formado por 9 dígitos, sendo os 8 primeiros dígitos aleatórios, e o último dígito é um dígito verificador. O dígito verificador é calculado através do módulo 10, onde cada dígito do RG é multiplicado por um peso, e o resultado é somado. O resultado da soma é dividido por 10, e o resto da divisão é subtraído de 10. Se o resultado da subtração for igual a 10, o dígito verificador é 0, caso contrário, o dígito verificador é o resultado da subtração.
    </p>
