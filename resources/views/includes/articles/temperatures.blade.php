    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        O que é temperatura?
    </h3>

    <p class="mb-2 sm:mb-3">
        A temperatura é uma medida que descreve o grau de calor ou frio de um objeto, substância ou ambiente. Ela está
        relacionada com a energia cinética das partículas que compõem o sistema em questão. Quanto mais quente algo
        estiver, maior será a temperatura, o que significa que suas partículas estão em movimento mais rápido. A
        temperatura pode ser medida em várias escalas, como Celsius, Fahrenheit ou Kelvin, e desempenha um papel
        fundamental em diversas áreas da ciência, engenharia e vida cotidiana, influenciando processos químicos, físicos
        e biológicos, bem como nossas sensações de conforto térmico.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        As principais escalas de temperatura
    </h3>

    <p class="mb-2 sm:mb-3">
        As três principais medidas de temperatura são Celsius, Fahrenheit e Kelvin. O Celsius é amplamente utilizado em
        todo o mundo e é baseado na escala de água, onde 0°C é o ponto de congelamento e 100°C é o ponto de ebulição da
        água a pressão atmosférica normal. O Fahrenheit é comum nos Estados Unidos e é baseado em uma escala que coloca
        o ponto de congelamento da água em 32°F e o ponto de ebulição em 212°F. Já o Kelvin é uma medida absoluta de
        temperatura usada principalmente na ciência, onde zero Kelvin (-273,15°C) representa o zero absoluto, a
        temperatura mais fria possível, onde todas as partículas cessam seu movimento térmico.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Celsius
    </h3>

    <p class="mb-2 sm:mb-3">
        A escala Celsius (°C) é uma medida de temperatura amplamente utilizada em todo o mundo. Ela é baseada em dois
        pontos de referência: 0°C representa o ponto de congelamento da água, enquanto 100°C representa o ponto de
        ebulição da água a pressão atmosférica normal ao nível do mar. A escala Celsius é considerada mais intuitiva do
        que a escala Fahrenheit, pois está diretamente relacionada às mudanças de estado da água, tornando-a fácil de
        entender e aplicar em situações cotidianas. Ela é comumente usada em contextos científicos, meteorológicos e
        para medir a temperatura em países que adotam o sistema métrico.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Kelvin
    </h3>

    <p class="mb-2 sm:mb-3">
        O Kelvin (K) é uma unidade de medida de temperatura absoluta que não utiliza valores negativos. A escala Kelvin
        começa no zero absoluto, a temperatura mais baixa possível, onde todas as partículas cessam seu movimento
        térmico. Portanto, em Kelvin, zero absoluto é igual a 0 K, enquanto o ponto de congelamento da água é 273,15 K,
        e o ponto de ebulição da água é 373,15 K. O Kelvin é amplamente utilizado na ciência, especialmente na física e
        na química, devido à sua natureza absoluta, tornando-o uma ferramenta importante para estudos que envolvem
        temperaturas extremas e cálculos precisos.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Fahrenheit
    </h3>

    <p class="mb-2 sm:mb-3">
        A escala Fahrenheit (°F) é uma medida de temperatura usada principalmente nos Estados Unidos e em alguns outros
        lugares. Ela foi desenvolvida por Daniel Gabriel Fahrenheit. Nessa escala, o ponto de congelamento da água
        ocorre a 32°F e o ponto de ebulição da água a 212°F, ambos a pressão atmosférica normal ao nível do mar. A
        escala Fahrenheit é conhecida por ser menos intuitiva do que a escala Celsius, que é mais amplamente usada em
        todo o mundo, mas ainda é usada em muitos contextos nos Estados Unidos, como para relatar a temperatura do clima
        e em algumas aplicações de engenharia específicas.
    </p>
