    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Números até Dez
    </h3>

    <p class="mb-2 sm:mb-3">
        Até o número dez, geralmente escrevemos por extenso, por exemplo, "um", "dois", "três", e assim por diante. Isso se aplica não apenas à contagem, mas também a quantidades, como "cinco quilos" ou "oito páginas".
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Dezenas, Centenas, Milhar e Milhão
    </h3>

    <p class="mb-2 sm:mb-3">
        Ao lidar com números nas dezenas, centenas, milhares e milhões, é necessário utilizar a conjunção "e" entre as unidades e os milhares, por exemplo, "dois mil e quinhentos" ou "um milhão e duzentos mil".
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Casos Especiais
    </h3>

    <p class="mb-2 sm:mb-3">
        Algumas expressões numéricas têm regras específicas, como anos, medidas de tempo e números ordinais. Ao mencionar anos, utiliza-se o termo "mil" entre o numeral e "anos", como em "mil novecentos e oitenta e quatro". Já em medidas de tempo, a expressão "anos-luz" é escrita com hífen.
    </p>
