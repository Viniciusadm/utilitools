    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Regra de três
    </h3>

    <p class="mb-2 sm:mb-3">
        A regra de três simples é um método matemático utilizado para encontrar um valor desconhecido com base em proporções estabelecidas. Ela se baseia na ideia de que se duas grandezas estão relacionadas de maneira proporcional, é possível calcular uma terceira grandeza com base nessa relação. Esse conceito é amplamente utilizado em diversas áreas, como matemática, física, economia e muitas outras, sendo uma ferramenta fundamental para resolver uma variedade de problemas do dia a dia. Em sua forma mais simples, a regra de três envolve a multiplicação cruzada de valores conhecidos para encontrar o valor desejado. É uma técnica poderosa e útil que facilita a resolução de muitos cálculos práticos.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Como calcular regra de três
    </h3>

    <ol class="list-decimal list-inside mb-2 sm:mb-3">
        <li class="mb-1"><span class="font-bold">Identificação das Grandezas:</span> Comece identificando claramente as duas grandezas que você conhece e a terceira que deseja encontrar. Essas grandezas são geralmente chamadas de A, B e X, respectivamente.</li>
        <li class="mb-1"><span class="font-bold">Estabelecimento da Proporção:</span> Crie uma proporção que relacione as duas grandezas conhecidas, geralmente no formato "A está para B assim como X está para Y".</li>
        <li class="mb-1"><span class="font-bold">Utilização da Multiplicação Cruzada:</span> A etapa crítica é aplicar a multiplicação cruzada. Isso significa multiplicar o valor de A pelo valor de C e o valor de B pelo valor de X.</li>
        <li class="mb-1"><span class="font-bold">Isolamento da Grandeza Desconhecida:</span> Para encontrar o valor desconhecido, divida o resultado da multiplicação cruzada de A por C pelo valor de B. Isso fornecerá o valor da grandeza desconhecida, X.</li>
    </ol>
