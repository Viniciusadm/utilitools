    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        O que é CNPJ?
    </h3>

    <p class="mb-2 sm:mb-3">
        Um CNPJ (Cadastro Nacional da Pessoa Jurídica) é um número único atribuído a cada empresa e entidade legal no Brasil. Ele serve como um identificador único para fins fiscais e administrativos e é emitido pela Receita Federal. O CNPJ é essencial para que as empresas possam operar legalmente no país, realizar transações financeiras, emitir notas fiscais e cumprir suas obrigações tributárias.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Como funciona o CNPJ?
    </h3>

    <p class="mb-2 sm:mb-3">
        O CNPJ é um número composto por 14 dígitos, sendo os dois últimos dígitos verificadores. Os oito primeiros dígitos representam a identificação da empresa, os quatro dígitos seguintes são utilizados para identificar a unidade de atuação da empresa, e os dois últimos dígitos são os verificadores. O CNPJ é armazenado em um banco de dados centralizado, mantido pela Receita Federal, e é utilizado para rastrear informações fiscais e comerciais das empresas.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Como obter um CNPJ?
    </h3>

    <p class="mb-2 sm:mb-3">
        Para obter um CNPJ, uma empresa deve se registrar na Receita Federal. O registro pode ser feito online ou pessoalmente em uma unidade da Receita Federal. O registro online é feito através do site da Receita Federal, e o registro presencial é feito através de um Centro Virtual de Atendimento ao Contribuinte (e-CAC). O registro online é mais rápido e fácil, mas requer que a empresa tenha um certificado digital.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Quando foi criado o CNPJ?
    </h3>

    <p class="mb-2 sm:mb-3">
        O CNPJ foi criado em 1998, através da Instrução Normativa SRF nº 27. A lei foi criada para substituir o antigo Cadastro Geral de Contribuintes (CGC), que foi criado em 1968 e era utilizado para identificar as empresas brasileiras.
    </p>
