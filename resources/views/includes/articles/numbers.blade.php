        O que são bases numéricas?
    </h3>

    <p class="mb-2 sm:mb-3">
        Uma base numérica é um sistema de numeração que usa um conjunto de símbolos para representar quantidades. A base numérica mais comum é a base 10, que usa os símbolos 0-9. Outras bases numéricas comuns incluem a base 2 (binária), a base 8 (octal) e a base 16 (hexadecimal).
        Nós usamos a base 10 em nosso dia a dia por causa de nossos 10 dedos. Mas computadores usam a base 2 porque eles usam circuitos eletrônicos que podem estar ligados ou desligados, representados por 0 e 1.
