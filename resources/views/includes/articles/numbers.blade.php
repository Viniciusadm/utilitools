    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Usos da base 2
    </h3>

    <p class="mb-2 sm:mb-3 whitespace-pre-line">A base numérica 2, também conhecida como sistema binário, é fundamental no mundo da computação e da eletrônica, pois representa informações usando apenas dois dígitos: 0 e 1. Esses dígitos são chamados de "bits" (abreviação de binary digit) e formam a linguagem básica dos computadores.
        Cada dígito em uma representação binária pode ser 0 ou 1, e a combinação de vários dígitos binários forma números maiores. Por exemplo, o número binário "1010" representa o número decimal 10.
        O sistema binário é amplamente utilizado em computadores para armazenar, processar e transmitir informações. Os circuitos eletrônicos internos dos computadores usam estados binários (ligado/desligado) para representar dados, tornando-o essencial para todas as operações de hardware e software. Além disso, os programas de computador, textos, imagens e todos os outros tipos de dados são armazenados e processados usando representações binárias.
        Em resumo, a base numérica 2, ou sistema binário, é a linguagem fundamental dos computadores, permitindo a representação eficiente de informações usando apenas dois dígitos (0 e 1), o que é essencial para a computação moderna.</p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Usos da base 8
    </h3>

    <p class="mb-2 sm:mb-3 whitespace-pre-line">A base numérica 8, também conhecida como sistema octal, é um sistema de numeração que utiliza oito dígitos diferentes: 0, 1, 2, 3, 4, 5, 6 e 7. O sistema octal é menos comum do que o sistema decimal (base 10) e o sistema binário (base 2), mas ainda é usado em algumas aplicações específicas.
        Cada dígito em uma representação octal pode variar de 0 a 7. Por exemplo, o número octal "54" representa o número decimal 44.
        O sistema octal costumava ser mais relevante na computação e na programação de computadores, especialmente em sistemas mais antigos. No entanto, ele foi em grande parte substituído pelo sistema hexadecimal (base 16), que é mais conveniente para representar valores binários em um formato mais compacto e legível.
        Em resumo, a base numérica 8, ou sistema octal, é um sistema de numeração que utiliza oito dígitos e é usado em algumas aplicações específicas, embora tenha sido em grande parte substituído pelo sistema hexadecimal em muitos contextos tecnológicos.</p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Usos da base 10
    </h3>

    <p class="mb-2 sm:mb-3 whitespace-pre-line">A base numérica 10, também conhecida como sistema decimal, é o sistema de numeração mais amplamente utilizado em todo o mundo. Ele utiliza dez dígitos diferentes: 0, 1, 2, 3, 4, 5, 6, 7, 8 e 9. A base 10 é fundamental na vida cotidiana e é usada para contar, calcular e representar quantidades em grande parte das culturas e aplicações.
        Cada dígito em uma representação decimal pode variar de 0 a 9. A posição de um dígito em um número determina o valor que ele contribui para o valor total do número. Por exemplo, no número decimal "247," o "7" está na posição das unidades, o "4" está na posição das dezenas e o "2" está na posição das centenas.
        O sistema decimal é amplamente utilizado em matemática, finanças, comércio, ciências e em praticamente todos os aspectos da vida diária. Ele é a base padrão para a representação de quantidades e cálculos em muitos contextos, e a maioria das pessoas está familiarizada com seu funcionamento desde a infância.
        Em resumo, a base numérica 10, ou sistema decimal, é o sistema de numeração mais comumente usado no mundo, empregando dez dígitos e sendo essencial para a representação e cálculo de quantidades em uma ampla variedade de aplicações.</p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Usos da base 16
    </h3>

    <p class="mb-2 sm:mb-3 whitespace-pre-line">A base numérica 16, também conhecida como sistema hexadecimal, é amplamente utilizada em ciência da computação e eletrônica, especialmente para representar valores binários de uma forma mais compacta e legível. O sistema hexadecimal utiliza 16 dígitos diferentes, incluindo os dígitos do sistema decimal (0 a 9) e seis dígitos adicionais representados pelas letras A a F (ou a-f) para representar os valores de 10 a 15.
        No sistema hexadecimal, é comum representar valores binários de maneira mais concisa. Cada dígito hexadecimal é equivalente a quatro dígitos binários (bits). Isso torna a conversão entre binário e hexadecimal conveniente para programadores e engenheiros de hardware.
        Por exemplo, o número binário "11011010" pode ser representado em hexadecimal como "DA" (onde D representa 13 e A representa 10 em decimal). Isso facilita a leitura e a escrita de números binários longos.
        O sistema hexadecimal é amplamente utilizado na programação de computadores, na representação de endereços de memória, na codificação de cores em gráficos e em várias outras aplicações relacionadas à computação e eletrônica.
        Em resumo, a base numérica 16, ou sistema hexadecimal, é fundamental na ciência da computação e eletrônica, permitindo a representação eficiente de valores binários e sendo amplamente utilizado em programação e outras aplicações técnicas.</p>
