    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Divisão
    </h3>

    <p class="mb-2 sm:mb-3 whitespace-pre-line">O cálculo de divisão é um processo matemático utilizado para determinar quantas vezes um número (divisor) cabe em outro número (dividendo) de maneira equitativa. Para realizá-lo, você começa dividindo o dividendo pelo divisor e obtém um quociente. Esse quociente representa o número de vezes que o divisor cabe no dividendo. Se houver um resto após a divisão, ele também é registrado. A fórmula básica é:
        Dividendo ÷ Divisor = Quociente com Resto
        Por exemplo, se dividirmos 10 por 3: 10 ÷ 3 = 3 com resto 1
        Isso significa que 3 vezes o número 3 cabe em 10, com um resto de 1. O quociente é 3, e o resto é 1. Esse é o funcionamento essencial de uma operação de divisão.</p>
