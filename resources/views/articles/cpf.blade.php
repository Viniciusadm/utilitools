    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        O que é CPF?
    </h3>

    <p class="mb-2 sm:mb-3">
        Um CPF (Cadastro de Pessoas Físicas) é um número único atribuído a cada cidadão brasileiro, residente ou não no Brasil. Serve como um identificador individual e é emitido pela Receita Federal. O CPF é essencial para realizar várias transações financeiras e para acessar serviços governamentais, como a declaração de imposto de renda. É uma parte fundamental da identificação pessoal no Brasil.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Como funciona o CPF?
    </h3>

    <p class="mb-2 sm:mb-3">
        O CPF é um número de 11 dígitos, sendo os dois últimos dígitos verificadores. Os nove primeiros dígitos são aleatórios e atribuídos pelo governo. Os dois últimos dígitos são calculados usando um algoritmo especial. O CPF é armazenado em um banco de dados centralizado, que é mantido pela Receita Federal.
    </p>

    <h3 class="text-xl font-semibold mb-2 sm:mb-3">
        Como eu sei de qual estado é o CPF?
    </h3>

    <p class="mb-1 sm:mb-2">
        Para determinar o estado brasileiro de emissão de um CPF, basta verificar o último algarismo, excluindo os dígitos de controle/verificadores. Por exemplo, no CPF XXX.XXX.XX6-XX, o último número válido é 6, indicando que o documento foi emitido em Minas Gerais, de acordo com a seguinte lista de códigos:
    </p>

    <ul class="list-none mb-2 sm:mb-3">
        <li><strong>0</strong> - Rio Grande do Sul</li>
        <li><strong>1</strong> - Distrito Federal, Goiás, Mato Grosso do Sul e Tocantins</li>
        <li><strong>2</strong> - Pará, Amazonas, Acre, Amapá, Rondônia e Roraima</li>
        <li><strong>3</strong> - Ceará, Maranhão e Piauí</li>
        <li><strong>4</strong> - Pernambuco, Rio Grande do Norte, Paraíba e Alagoas</li>
        <li><strong>5</strong> - Bahia e Sergipe</li>
        <li><strong>6</strong> - Minas Gerais</li>
        <li><strong>7</strong> - Rio de Janeiro e Espírito Santo</li>
        <li><strong>8</strong> - São Paulo</li>
        <li><strong>9</strong> - Paraná e Santa Catarina</li>
    </ul>

